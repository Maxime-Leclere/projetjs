
class Cocktail {
    constructor(id, title, description, detail) {
        this.id = id;
        this.title = title;
        this.description = description;
        this.detail = detail;
    }

    getId() {
        return this.id;
    }

    getTitle() {
        return this.title;
    }

    getDescription() {
        return this.description;
    }

    getDetail() {
        return this.detail;
    }
}
